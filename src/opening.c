/**
 * Normalize a char, ignoring accents and font case.
 *
 * @param c char input.
 * @return converted char.
 */
static int to_lower_no_accent(int c)
{
#ifdef UNICODE
	c = towlower(c);
	if (c == L'é' || c == L'è' || c == L'ê')	return 'e';
	if (c == L'à')	return 'a';
	if (c == L'ï')	return 'i';
#else
	c = tolower(c);
	if (c == 'é' || c == 'è' || c == 'ê')	return 'e';
	if (c == 'à')	return 'a';
	if (c == 'ï')	return 'i';
#endif
	return c;
}

/**
 * Compare two string, ignoring accents, font case, etc.
 *
 * @param s1 first string.
 * @param s2 second string.
 */
static bool match(const char *s1, const char *s2)
{
	int c1, c2;

	for (;;) {
		while (isspace(*s1) || ispunct(*s1)) ++s1;
		while (isspace(*s2) || ispunct(*s2)) ++s2;

		if (*s1 == '\0' || *s2 == '\0') return (*s2 == '\0');

		c1 = *s1++;
		c2 = *s2++;
#ifdef UNICODE
		if ("ê"[0] == '\xc3') {	// test source encoding
			if (c1 & 0x80) {	// utf8 to UCS2
				c1 &= 0x1f;
				while ((*s1 & 0xc0) == 0x80)
					c1 = (c1 << 6) | (*s1++ & 0x3f);
			}
			if (c2 & 0x80) {
				c2 &= 0x1f;
				while ((*s2 & 0xc0) == 0x80)
					c2 = (c2 << 6) | (*s2++ & 0x3f);
			}
		}
#endif
		c1 = to_lower_no_accent(c1);
		c2 = to_lower_no_accent(c2);

		if (c1 != c2) return false;
	}
}

